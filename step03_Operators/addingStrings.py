# ?  How to ADD a string and a variable in a single print statement using + operator
name = "Saad Irfan"
print("Hi my name is" + " " + name)

# ? Another way is just using a comma ','
print("Hi my name is ", name)

# ? Another way is using f string
print(f"Hi my name is {name}")

# ? Adding number with string
age = 21
print("My age is " + str(age))
# ? Or by using comma
print("My age is ", age)
# ? Or by using f string
print(f"My age is {age:.2F}")
